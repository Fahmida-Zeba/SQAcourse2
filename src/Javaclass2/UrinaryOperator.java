package Javaclass2;

public class UrinaryOperator {
    public static void main(String[] args) {

        int a = 20;
        System.out.println("urinary plus of post:" + a++);
        System.out.println("urinary plus of pre:" + ++a);
        System.out.println("urinary minus of pre:" + --a);
        System.out.println("urinary minus of post:" + a--);
        System.out.println("urinary minus of post:" + a);
    }
    }


